# 3. Faça um programa que receba quatro números inteiros, calcule e mostre a soma desses números
n1 = int(input("Digite o 1° numero para soma: "))
n2 = int(input("Digite o 2° numero para soma: "))
n3 = int(input("Digite o 3° numero para soma: "))
n4 = int(input("Digite o 4° numero para soma: "))

soma = n1 + n2 + n3 + n4
print(f"Somados {n1}, {n2}, {n3}, {n4}, temos o valor de {soma}")
